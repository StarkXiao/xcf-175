export const WORLD_EVENT_RARITY_CONFIG: Record<string, { name: string; color: string; weight: number }> = {
  common: { name: '普通', color: '#aaaaaa', weight: 40 },
  rare: { name: '稀有', color: '#4488ff', weight: 30 },
  epic: { name: '史诗', color: '#aa00ff', weight: 20 },
  legendary: { name: '传说', color: '#ffaa00', weight: 10 },
};

export const CYCLE_DURATION_HOURS = 4;

export function getWorldEventTemplate(id: string): WorldEventTemplate | undefined {
  return WORLD_EVENT_TEMPLATES.find(t => t.id === id);
}

export function getBattleRuleConfig(rule: string): WorldEventBattleRuleConfig | undefined {
  return BATTLE_RULE_CONFIGS[rule];
}

export function pickRandomEvent(): WorldEventTemplate {
  const weights = WORLD_EVENT_TEMPLATES.map(t => {
    const config = WORLD_EVENT_RARITY_CONFIG[t.rarity];
    return config?.weight ?? 10;
  });
  const totalWeight = weights.reduce((a, b) => a + b, 0);
  let roll = Math.random() * totalWeight;
  for (let i = 0; i < WORLD_EVENT_TEMPLATES.length; i++) {
    roll -= weights[i];
    if (roll <= 0) return WORLD_EVENT_TEMPLATES[i];
  }
  return WORLD_EVENT_TEMPLATES[WORLD_EVENT_TEMPLATES.length - 1];
}

export function getActiveBattleRules(eventTemplate: WorldEventTemplate): WorldEventBattleRuleConfig[] {
  return eventTemplate.battleRules
    .map(rule => BATTLE_RULE_CONFIGS[rule])
    .filter(Boolean);
}
