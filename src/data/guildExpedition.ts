const MEMBER_AVATARS = ['🦊', '🐺', '🦅', '🐍', '🦁', '🐯', '🐻', '🦈', '🐉', '🦇'];

export const createGuildMembers = (count: number = 5): GuildMember[] => {
  const members: GuildMember[] = [];
  for (let i = 0; i < count; i++) {
    members.push({
      id: generateId('member'),
      name: MEMBER_NAMES[i % MEMBER_NAMES.length],
      avatar: MEMBER_AVATARS[i % MEMBER_AVATARS.length],
      level: Math.floor(Math.random() * 15) + 5,
      contribution: Math.floor(Math.random() * 500),
      weeklyContribution: Math.floor(Math.random() * 200),
      teamAnimalIds: [],
      isOnline: Math.random() > 0.3,
    });
  }
  return members;
};

export const getBossTemplate = (id: string): GuildBossTemplate | undefined => {
  return GUILD_BOSS_TEMPLATES.find(b => b.id === id);
};

export const getAvailableBosses = (guildLevel: number): GuildBossTemplate[] => {
  return GUILD_BOSS_TEMPLATES.filter(b => {
    if (b.rarity <= 3) return true;
    if (b.rarity === 4) return guildLevel >= 2;
    return guildLevel >= 3;
  });
};
