export const getMaterialTemplate = (id: string): MaterialTemplate | undefined => {
  return MATERIAL_TEMPLATES.find(m => m.id === id);
};

export const getBreakthroughMaterialsByElement = (element: Element, tier: number): string[] => {
  const suffix = Math.min(tier, 4) as 1 | 2 | 3 | 4;
  const templates = MATERIAL_TEMPLATES.filter(
    m => m.type === 'breakthrough' && m.element === element && m.rarity <= suffix
  );
  return templates.map(m => m.id);
};

export const getStarMaterialByRarity = (rarity: number): string => {
  return `mat_star_shard_${rarity}`;
};
