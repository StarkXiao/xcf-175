export const getTaskTemplate = (id: string): TaskTemplate | undefined => {
  return TASK_TEMPLATES.find(t => t.id === id);
};

export const getTasksByCategory = (category: TaskCategory): TaskTemplate[] => {
  return TASK_TEMPLATES.filter(t => t.category === category);
};

export const getDailyTasks = (): TaskTemplate[] => {
  return TASK_TEMPLATES.filter(t => t.isDaily);
};

export const getAchievements = (): TaskTemplate[] => {
  return TASK_TEMPLATES.filter(t => t.isAchievement);
};

export const getTaskCategoryLabel = (category: TaskCategory): string => {
  const labels: Record<TaskCategory, string> = {
    battle: '战斗',
    gacha: '抽取',
    ascend: '养成',
    lineup: '编队',
    daily: '每日任务',
    achievement: '成就',
  };
  return labels[category];
};

